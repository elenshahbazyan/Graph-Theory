n = len(graph)
count = 0
components = [-1 for i in range(n)]
visited = [False for i in range(n)]

def FindComp():
  global count
  count = 0
  for i in range(n):
    if not visited[i]:
      count += 1
      dfs(i, count)
  return count, components

def dfs(at, comp_id):
  visited[at] = True
  components[at] = comp_id
  for next in range(n):
    if graph[at][next] == 1 and not visited[next]:
      dfs(next, comp_id)

